package com.bsep.recipes.dto;

public class StepDTO {
	String step;
	Boolean success;
	public StepDTO() {
		super();
	}
	public String getStep() {
		return step;
	}
	public void setStep(String step) {
		this.step = step;
	}
	public Boolean getSuccess() {
		return success;
	}
	public void setSuccess(Boolean success) {
		this.success = success;
	}
	
}
